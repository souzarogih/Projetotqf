package com.moresales.lambda.model;

public class Calculadora {
    public float soma(float val1, float val2){
        return val1+val2;
    }

    public float subtrair(float val1, float val2){
        return val1-val2;
    }

    public float multiplicar(float val1, float val2){
        return val1*val2;
    }

    public float dividir(float val1, float val2){
        return val1 / val2;
    }
}
