package com.moresales.lambda;

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello world!");
    }

    private float soma(float val1, float val2){
        return val1+val2;
    }

    private float subtrair(float val1, float val2){
        return val1-val2;
    }

    private float multiplicar(float val1, float val2){
        return val1*val2;
    }

    private float dividir(float val1, float val2){
        return val1/val2;
    }
}